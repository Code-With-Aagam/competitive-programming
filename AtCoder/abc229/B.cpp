template <typename... T>
void read(T &...args) {
	((cin >> args), ...);
}

template <typename... T>
void write(T &&...args) {
	((cout << args), ...);
}

template <typename T>
void readContainer(T &t) {
	for (auto &e : t) {
		read(e);
	}
}

template <typename T>
void writeContainer(T &t) {
	for (const auto &e : t) {
		write(e, " ");
	}
	write("\n");
}

void solve(int tc) {
	int a, b;
	read(a, b);
	int ans = 0, carry = 0;
	bool easy = true;
	while (a > 0 and b > 0) {
		int x = a % 10;
		a /= 10;
		int y = b % 10;
		b /= 10;
		if ((x + y + carry) >= 10) {
			easy = false;
			break;
		}
		ans *= 10;
		ans += (x + y + carry) % 10;
		carry = (x + y + carry) / 10;
	}
	// deb(ans);
	write((easy ? "Easy" : "Hard"));
}

