template <typename T>
using min_heap = priority_queue<T, vector<T>, greater<T>>;

template <typename T>
using max_heap = priority_queue<T>;

template <class T>
using ordered_set = tree<T, null_type, less_equal<T>, rb_tree_tag, tree_order_statistics_node_update>;

template <typename... T>
void read(T &...args) {
	((cin >> args), ...);
}

template <typename... T>
void write(T &&...args) {
	((cout << args), ...);
}

template <typename T>
void readContainer(T &t) {
	for (auto &e : t) {
		read(e);
	}
}

template <typename T>
void writeContainer(T &t) {
	for (const auto &e : t) {
		write(e, " ");
	}
	write("\n");
}

void solve() {
	int n;
	read(n);
	vector<string> arr(n);
	readContainer(arr);
	set<string> st;
	for (const auto &ele : arr) {
		string curr = ele;
		reverse(all(curr));
		if (st.count(curr) > 0 or curr == ele or st.count(curr.substr(0, 2)) > 0) {
			write("YES\n");
			return;
		}
		for (char ch = 'a'; ch <= 'z'; ++ch) {
			string s = curr + ch;
			if (st.count(s) > 0) {
				write("YES\n");
				return;
			}
		}
		st.insert(ele);
	}
	write("NO\n");
}

signed main() {
	ios::sync_with_stdio(false);
	cin.tie(nullptr);
	int T = 1;
	read(T);
	for (int t = 1; t <= T; ++t) {
		solve();
	}
}